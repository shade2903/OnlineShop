package com.haiduk.domain;

public class User {

    private int id;
    private String name;

    public User() {
    }
    public int getId() {
        return id;
    }


    public void setId(int id) {
        this.id = id;
    }



    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

}
